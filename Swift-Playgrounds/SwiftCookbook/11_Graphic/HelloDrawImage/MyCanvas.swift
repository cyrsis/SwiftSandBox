import UIKit

class MyCanvas: UIView {
    
    override func draw(_ rect: CGRect) {
        let image = UIImage(named: "Beautiful")            //讀入圖片
        image?.draw(at: CGPoint(x: 0, y: 0))              //第一種方法
        image?.draw(in: CGRect(x: 0, y: 350, width: 300, height: 300))    //第二種方法
    }    
}
